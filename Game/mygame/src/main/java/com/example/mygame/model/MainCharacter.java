package com.example.mygame.model;

import java.awt.image.BufferedImage;
public class MainCharacter {
    private int x; // x-coordinate of the main character
    private int y; // y-coordinate of the main character
    private int width; // width of the main character
    private int height; // height of the main character
    private BufferedImage image; // image of the main character

    public MainCharacter(int x, int y, int width, int height, BufferedImage image) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.image = image;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public BufferedImage getImage() {
        return image;
    }

    public void setImage(BufferedImage image) {
        this.image = image;
    }
}
